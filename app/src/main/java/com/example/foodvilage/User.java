package com.example.foodvilage;

public class User {
    private String first_name, last_name, email, contact;

    public User() {
    }

    public User(String first_name, String last_name, String email, String contact) {
        this.first_name = first_name;
        this.last_name = last_name;
        this.email = email;
        this.contact = contact;
    }
}
